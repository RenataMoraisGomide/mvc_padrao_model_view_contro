# É um padrão muito utilizado, organizado, que podemos trabalhar com várias pessoas ao mesmo tempo 
# Dividir o código em partes separadas que fazem relações com as outras
# M-V-C é model, view e controller
# => View = é a parte do seu projeto responável pela interface do projeto. (o que o usuário vai ver) html, django, tkinter
# => Model = é responsável pelo armazenamento e estrutura de dados, (classes, por exemplo), quais os dados, tipos de dados
# => Controller = é responsável pela lógica do problema, a lógica mais pesada, tratar os dados, (ex: ver se o cpf é válido)

